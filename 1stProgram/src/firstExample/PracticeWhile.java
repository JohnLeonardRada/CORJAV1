package firstExample;

public class PracticeWhile {

	public static void main(String[] args) {
		// Using While Loop
		int x = -10;
		// While Loop with Increment Operator
		while(x <= 0) {
			System.out.println("x = " + x);
			x++; 
			/*Alternate Version
			 * x = x + 1;
			 * x += 1;
			 */
		}
	}

}
