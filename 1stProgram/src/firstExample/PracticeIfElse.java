package firstExample;

public class PracticeIfElse {
	
	public static void main(String[] args) {
		// Using If-Else
		// X and Y Example
		int x = 20;
		int y = 20;
		
		if (x < y) {
			System.out.println("X is less than Y");
		}else if (x == y) {
			System.out.println("X is equal to Y");
		}else {
			System.out.println("X is greater than Y");
		}
		
	}

}
